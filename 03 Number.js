let integer = 22; // integer number 
console.log(integer); // Print the value of the variable integer
console.log(integer.toString()); // convert integer to string
console.log(parseFloat(integer)); // convert integer to float
console.log(Number.isInteger(integer)); // check integer
console.log(Number.isNaN(integer)); // check not a number
console.log(Number.isFinite(integer)); // check integer is a finite number
let float = 22.22; // float number
console.log(float); // Print the value of the variable float
console.log(parseInt(float)); // convert float to integer
console.log(Number.isInteger(float)); // check float
console.log(isNaN(float)); // check not a number
console.log(float.toString()); // convert float to string
console.log(Math.round(float)); // convert float to integer
console.log(Math.floor(float)); // convert float to integer
console.log(Math.ceil(float)); // convert float to integer
console.log(Math.pow(float, 2)); // math power
console.log(Math.sqrt(float)); // math square root
console.log(-float); // convert positive float to negative float
console.log(Math.abs(-float)); // convert negative float to positive float
console.log(Math.min(float, 3, 6, 10)); // find minimum number
console.log(Math.max(float, 3, 6, 10, 25)); // find maximum number
console.log(Math.random()); // generate random number